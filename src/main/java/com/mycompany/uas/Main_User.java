    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
        int row = user.getSelectedRow();
        if (row == -1) {
            JOptionPane.showMessageDialog(this, "Pilih user yang akan diedit!");
            return;
        }

        int id = Integer.parseInt(user.getValueAt(row, 0).toString());
        String username = user.getValueAt(row, 1).toString();
        String role = user.getValueAt(row, 2).toString();
        
        this.setVisible(false);
        Edit_User eu = new Edit_User(currentUsername, id, username, role);
        eu.setVisible(true);
    }//GEN-LAST:event_editMouseClicked

    private void deleteMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_deleteMouseClicked
        // TODO add your handling code here:
        int row = user.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih user yang akan dihapus.",
                    "Info", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id = Integer.parseInt(user.getValueAt(row, 0).toString());
        String username = user.getValueAt(row, 1).toString();

        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Yakin ingin menonaktifkan user: " + username + " ?",
                "Konfirmasi",
                JOptionPane.YES_NO_OPTION);

        if (konfirmasi != JOptionPane.YES_OPTION) return;

        String sql = "UPDATE users SET is_active = 0 WHERE id = ?";

        try (Connection conn = Koneksi.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, id);
            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this,
                        "User berhasil dinonaktifkan.",
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                loadUser(); // refresh tabel
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal menonaktifkan user: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_deleteMouseClicked

    private void activeMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_activeMouseClicked
        // TODO add your handling code here:
        int row = user.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih user yang akan di-restore.",
                    "Info", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id = Integer.parseInt(user.getValueAt(row, 0).toString());
        String username = user.getValueAt(row, 1).toString();

        int konfirmasi = JOptionPane.showConfirmDialog(this,
                "Restore user: " + username + " ?",
                "Konfirmasi Restore",
                JOptionPane.YES_NO_OPTION);

        if (konfirmasi != JOptionPane.YES_OPTION) return;

        String sql = "UPDATE users SET is_active = 1 WHERE id = ?";

        try (Connection conn = Koneksi.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, id);
            int rows = ps.executeUpdate();

            if (rows > 0) {
                JOptionPane.showMessageDialog(this,
                        "User berhasil di-restore (aktif kembali).",
                        "Success", JOptionPane.INFORMATION_MESSAGE);
                loadUser();
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal restore user: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_activeMouseClicked

