    private void initTable() {
        tableModel = new DefaultTableModel(
                new Object[]{"ID", "Username", "Role", "Status"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        user.setModel(tableModel);

        // Sembunyikan kolom ID (kolom 0)
        user.getColumnModel().getColumn(0).setMinWidth(0);
        user.getColumnModel().getColumn(0).setMaxWidth(0);
        user.getColumnModel().getColumn(0).setWidth(0);
    }
    private void loadUser() {
        tableModel.setRowCount(0);

        String sql =
            "SELECT id, username, role, is_active " +
            "FROM users " +
            "WHERE (? IS NULL OR username <> ?) " +
            "ORDER BY id ASC";

        try (Connection conn = Koneksi.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setString(1, currentUsername);
            ps.setString(2, currentUsername);

            try (ResultSet rs = ps.executeQuery()) {
                while (rs.next()) {
                    int id = rs.getInt("id");
                    String username = rs.getString("username");
                    String role = rs.getString("role");
                    int isActive = rs.getInt("is_active");
                    String status = (isActive == 1) ? "Aktif" : "Nonaktif";

                    tableModel.addRow(new Object[]{id, username, role, status});
                }
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal load data user: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }



