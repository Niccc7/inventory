    private void editMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_editMouseClicked
        // TODO add your handling code here:
        int row = tblProduk.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih data produk yang akan diedit dari tabel.",
                    "Info", JOptionPane.WARNING_MESSAGE);
            return;
        }

        int id;
        try {
            id = Integer.parseInt(tblProduk.getValueAt(row, 0).toString());
        } catch (NumberFormatException e) {
            JOptionPane.showMessageDialog(this,
                    "ID produk tidak valid: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
            return;
        }
        String sku   = (String) tblProduk.getValueAt(row, 1);
        String nama  = (String) tblProduk.getValueAt(row, 2);
        String satuan= (String) tblProduk.getValueAt(row, 3);

        this.setVisible(false);
        Edit_Produk formEdit = new Edit_Produk(id, sku, nama, satuan);
        formEdit.setVisible(true);
    }//GEN-LAST:event_editMouseClicked

    private void hapusMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_hapusMouseClicked
        // TODO add your handling code here:
        int row = tblProduk.getSelectedRow();

        if (row == -1) {
            JOptionPane.showMessageDialog(this,
                    "Pilih produk yang akan dihapus dari tabel.",
                    "Info", JOptionPane.WARNING_MESSAGE);
            return;
        }

        // Ambil ID produk dari kolom tersembunyi (kolom 0)
        int id = Integer.parseInt(tblProduk.getValueAt(row, 0).toString());

        int konfirmasi = JOptionPane.showConfirmDialog(
                this,
                "Yakin ingin menghapus produk ini?",
                "Konfirmasi Hapus",
                JOptionPane.YES_NO_OPTION
        );

        if (konfirmasi != JOptionPane.YES_OPTION) {
            return;
        }

        String sql = "DELETE FROM products WHERE id = ?";

        try (Connection conn = Koneksi.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql)) {

            ps.setInt(1, id);
            ps.executeUpdate();

            JOptionPane.showMessageDialog(this,
                    "Produk berhasil dihapus!",
                    "Success", JOptionPane.INFORMATION_MESSAGE);

            loadProduk(); // refresh tabel

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal menghapus produk: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_hapusMouseClicked

