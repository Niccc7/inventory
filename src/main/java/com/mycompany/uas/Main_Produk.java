    private void initTable() {
        tableModel = new DefaultTableModel(
                new Object[]{"ID", "Code", "Nama Produk", "Satuan"}, 0) {
            @Override
            public boolean isCellEditable(int row, int column) {
                return false;
            }
        };

        tblProduk.setModel(tableModel);

        // Sembunyikan kolom ID (kolom 0)
        tblProduk.getColumnModel().getColumn(0).setMinWidth(0);
        tblProduk.getColumnModel().getColumn(0).setMaxWidth(0);
        tblProduk.getColumnModel().getColumn(0).setWidth(0);
    }


    private void loadProduk() {
        tableModel.setRowCount(0);

        String sql = "SELECT id, code, name, satuan FROM products ORDER BY id ASC";

        try (Connection conn = Koneksi.getConnection();
             PreparedStatement ps = conn.prepareStatement(sql);
             ResultSet rs = ps.executeQuery()) {

            while (rs.next()) {
                Object[] row = new Object[]{
                        rs.getInt("id"), 
                        rs.getString("code"),
                        rs.getString("name"), 
                        rs.getString("satuan")
                };
                tableModel.addRow(row);
            }

        } catch (SQLException e) {
            JOptionPane.showMessageDialog(this,
                    "Gagal load data produk: " + e.getMessage(),
                    "Error", JOptionPane.ERROR_MESSAGE);
        }
    }


